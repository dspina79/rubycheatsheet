# Comparisons 

# Basic value comparison

# greater than or equal
puts 5 >= 4 # outputs true

# greather than
puts 5 > 9 # outputs false

# equal
puts 6 == 7 # outputs false

# less than
puts 10 < 100 # outputs true

# less than or equal
puts 15 <= (20 - 5) # outputs true


# straight comparison (<=>)
puts 5 <=> 10  # outputs -1
puts 10 <=> 5  # outputs 1
puts 10 <=> 10 # outputs 0
