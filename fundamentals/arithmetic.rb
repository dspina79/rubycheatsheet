# Basic Arithmetic

# starting numbers
x = 5
y = 3

# addition
sum = x + y
puts sum # outputs 8

# subtraction
difference = x - y
puts difference # outputs 2

# multiplication
product = x * y
puts product # outputs 15

# division
quotient = x / y
puts quotient # outputs 1

# division with significant figures
quotient2 = 5.0 / 3.0
puts quotient2 # outputs 1.6666666666666667

# modulus
mod = y % x 
puts mod # outputs 2

# exponentiation
result = x ** y
puts result # outputs 125