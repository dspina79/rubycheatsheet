module Maths
    def Maths.add(x, y)
        return x + y
    end

    def Maths.subtract(x, y)
        return x - y
    end

    def Maths.multiply(x, y)
        return x * y
    end
    
    def Maths.divide(x, y)
        return x / y
    end

    def Maths.square(x)
        return x * x
    end
end